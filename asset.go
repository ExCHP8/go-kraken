package kraken

// Asset defines an asset within Kraken.
type Asset string

// AssetPair defines an asset pair within Kraken.
type AssetPair string

// AssetClass defines an asset class within Kraken.
type AssetClass string

const (
	// Currency is a currency asset class.
	Currency AssetClass = "currency"
)

// AssetInfo defines the information about an asset.
type AssetInfo struct {
	Altname         string     `json:"altname"`
	AssetClass      AssetClass `json:"aclass"`
	Decimals        int        `json:"decimals"`
	DisplayDecimals int        `json:"display_decimals"`
	Status          string     `json:"status"`
}

// Assets defines a map of assets.
type Assets map[Asset]AssetInfo

// Info returns the information about an asset.
func (a Assets) Info(asset Asset) AssetInfo {
	return a[asset]
}

// FeeTuple defines a schedule tuple.
// [<volume>, <percent fee>]
type FeeTuple []float64

// AssetPairInfo defines the information about an asset pair.
type AssetPairInfo struct {
	Altname            string     `json:"altname"`
	WSName             string     `json:"wsname"`
	AClassBase         AssetClass `json:"aclass_base"`
	Base               string     `json:"base"`
	AClassQuote        AssetClass `json:"aclass_quote"`
	Quote              string     `json:"quote"`
	PairDecimals       int        `json:"pair_decimals"`
	CostDecimals       int        `json:"cost_decimals"`
	LotDecimals        int        `json:"lot_decimals"`
	LotMultiplier      int        `json:"lot_multiplier"`
	LeverageBuy        []int      `json:"leverage_buy"`
	LeverageSell       []int      `json:"leverage_sell"`
	Fees               []FeeTuple `json:"fees"`
	FeesMaker          []FeeTuple `json:"fees_maker"`
	FeeVolumeCurrency  string     `json:"fee_volume_currency"`
	MarginCall         int        `json:"margin_call"`
	MarginStop         int        `json:"margin_stop"`
	OrderMin           string     `json:"ordermin"`
	CostMin            string     `json:"costmin"`
	TickSize           string     `json:"tick_size"`
	Status             Status     `json:"status"`
	LongPositionLimit  int        `json:"long_position_limit"`
	ShortPositionLimit int        `json:"short_position_limit"`
}

// AssetPairs defines a map of tradeable asset pairs.
type AssetPairs map[AssetPair]AssetPairInfo

// Info returns the information about an asset pair.
func (a AssetPairs) Info(assetPair AssetPair) AssetPairInfo {
	return a[assetPair]
}

